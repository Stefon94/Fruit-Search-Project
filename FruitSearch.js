const userInput = document.getElementById("fruit");
const suggestions = document.querySelector(".suggestions");

userInput.onkeyup = function(){
	let results = []
	let input = userInput.value;
	if(input.length){
		results = fruits.filter((searchHandler)=> {
			return searchHandler.toLowerCase().includes(input.toLowerCase());
		});
	}
	display(results);

	if (!results.length){
		suggestions.innerHTML = '';
	}
}

function display(results){
	const content = results.map((list)=>{
		return "<li onclick=selectInput(this)>" + list + "</li>";
	});
suggestions.innerHTML = "<ul>" + content.join('') + "</ul>";
}

function selectInput(list) {
	userInput.value = list.innerHTML;
	suggestions.innerHTML = '';
}